char	*ft_strchr(const char *s, int c)
{
	char	*p_s;

	p_s = (char *) s;
	if (*p_s == c)
		return (p_s);
	while (*p_s != '\0')
	{
		if (*p_s == c)
			return (p_s);
		p_s++;
	}
	return (0);
}
